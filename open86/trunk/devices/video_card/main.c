int VideoRefresh(void *dat){
	unsigned int ptr;
	unsigned int x,y;
	GlobStruct->InstallMessageEvent(VIDEO_CARD,Events);
	while(*GlobStruct->DevicesDone==0){}

	for(;;){
		GlobStruct->LockCPU();
		ptr=video_memory;
    for(y=0;y<rows;y++){
    	for(x=0;x<columns;x++){
			PutChar(GlobStruct->MemRead8(ptr),GlobStruct->MemRead8(ptr+1),x,y);
			ptr++;
			ptr++;
			}
		}
	GlobStruct->UnlockCPU();

	GlobStruct->PutMessage(GUI,DISPLAY_GRAPHICS,width*height*(colors/8),NULL,NULL,(void*)buffer,NULL);
	//GlobStruct->PutMessage(PIC_DEVICE,SEND_INTERRUPT,2,NULL,NULL,NULL,NULL);
	sleep(70);
	//render();
	}

}


