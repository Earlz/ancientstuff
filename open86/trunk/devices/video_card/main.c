//Color Lookup Table..converts 4bit colors to 8bit colors
unsigned char CLT[16]={0,0x80,0x80,0x80,0x80,0x80,0x80,0x80,0x80,0x80,0x80,0x80,0x80,0x80,0x80,0xFF};

inline void PutPixel(unsigned int x,unsigned int y,unsigned char color){
	unsigned int offset;

	offset=x+(width*y);
	buffer[offset]=color;
}

//note that X and Y are row and colounm
inline void PutChar(unsigned char chr,unsigned char color, unsigned int x, unsigned int y){
	unsigned int basex,basey;
	unsigned char tmp;
	basex=x*8;
	basey=y*8;
	//if(chr=='a'){printf("it's A\n");}
	for(y=0;y<=7;y++){
		for(x=0;x<=7;x++){
			tmp=RomFont[(chr*8)+y];
			tmp=tmp&((1<<x));
			tmp=tmp>>x;
			if(tmp==0){
				tmp=(color&0xF0)>>4;
			}else{
				tmp=(color&0x0F);
			}
			PutPixel(basex+x,basey+y,CLT[tmp]);
		}
	}
}



